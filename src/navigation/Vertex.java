package navigation;

/**
 * Represents a vertex in a graph.
 */
public class Vertex {
    /**
     * The id of this vertex.
     */
    private String id;

    /**
     * Initializes a vertex based on the given id.
     * @param id The id of the vertex.
     */
    public Vertex(String id){
        this.id = id;
    }
}
